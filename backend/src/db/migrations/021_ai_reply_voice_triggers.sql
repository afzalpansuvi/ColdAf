  CHECK (current_traffic_share BETWEEN 0.00 AND 100.00);
ALTER TABLE ab_variants ADD COLUMN IF NOT EXISTS is_winner BOOLEAN NOT NULL DEFAULT FALSE;

-- ---------------------------------------------------------------------------
-- 6. Deliverability Advisor Scores (per-organization, per-day)
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS deliverability_scores (
  id                    UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id       UUID NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
  smtp_account_id       UUID REFERENCES smtp_accounts(id) ON DELETE CASCADE,
  brand_id              UUID REFERENCES brands(id) ON DELETE CASCADE,
  -- Score components (0-100 each)
  overall_score         INTEGER NOT NULL
                          CHECK (overall_score BETWEEN 0 AND 100),
  auth_score            INTEGER NOT NULL DEFAULT 0, -- SPF/DKIM/DMARC
  reputation_score      INTEGER NOT NULL DEFAULT 0, -- Domain/IP reputation
  engagement_score      INTEGER NOT NULL DEFAULT 0, -- Open/click/reply rates
  infrastructure_score  INTEGER NOT NULL DEFAULT 0, -- Warmup, dedicated IP, tracking domain
  content_score         INTEGER NOT NULL DEFAULT 0, -- Spam words, formatting
  -- Detailed metrics
  metrics_snapshot      JSONB DEFAULT '{}',
  -- Recommendations
  recommendations       TEXT[] DEFAULT '{}',
  -- Date
  scored_at             TIMESTAMP DEFAULT NOW(),
  score_date            DATE NOT NULL DEFAULT CURRENT_DATE,
  -- Unique per org+date+smtp
  CONSTRAINT deliverability_scores_unique UNIQUE (organization_id, score_date, smtp_account_id)
);

CREATE INDEX IF NOT EXISTS idx_deliverability_scores_org ON deliverability_scores(organization_id);
CREATE INDEX IF NOT EXISTS idx_deliverability_scores_date ON deliverability_scores(score_date);
CREATE INDEX IF NOT EXISTS idx_deliverability_scores_smtp ON deliverability_scores(smtp_account_id);

